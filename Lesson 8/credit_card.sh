#!/bin/bash 

# Пошук в результатах виводу echo, Примітивна перевірка, адже картки різних банків мають різні формати і там все складніше
#https://stackoverflow.com/questions/9315647/regex-credit-card-number-tests

echo "5487-1254-5485-1223 12/23" | awk '/^[0-9]{4}-[0-9]{4}-[0-9]{4}-[0-9]{4}\s[0,1][1-9]\/[2,3][0-9]$/ {print $0}'