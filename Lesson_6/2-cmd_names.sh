file=usr_executables.txt

#виведення кількості рядків у файлі - напростіший спосіб. Інший спосіб - перебирання циклом. Оскільки команда видає кількість рядків і через пробіл назву файлу - використано роздільник
wc -l $file | cut -d ' ' -f1

#виведення тільки назв файлів - команда basename виділяє лише назву файлу, має синтаксис, що дозволяє виділяти розширення тощо. Але тут це не вимагається.

for line in $(head -10 $file)
do
name=$(basename $line)
echo $name >> cmd_names.txt
done